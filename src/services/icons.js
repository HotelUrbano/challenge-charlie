export function defineIcons(desc){
    if(desc === "chuva fraca"){
        return(
            "Q"
        )
    }
    if(desc === "céu pouco nublado"){
        return(
            "H"
        )
    }
    if(desc === "céu limpo" || desc === "ensolarado"){
        return(
            "B"
        )
    }
    if(desc ==="nuvens dispersas"){
        return(
            "S"
        )
    }
    if(desc ==="nuvens quebradas"){
        return(
            "Y"
        )
    }
}